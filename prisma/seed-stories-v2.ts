// Брендовая SVG-обложка как data-URI (CSP разрешает data:)
function cover(opts: { tag: string; lines: string[]; from: string; to: string; ink?: string }) {
  const ink = opts.ink ?? "#18181b";
  const titleSvg = opts.lines
    .map((line, i) => `<text x="60" y="${250 + i * 60}" font-family="Arial, sans-serif" font-size="46" font-weight="800" fill="${ink}">${escapeXml(line)}</text>`)
    .join("");
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="1200" height="630" viewBox="0 0 1200 630">
  <defs><linearGradient id="g" x1="0" y1="0" x2="1" y2="1">
    <stop offset="0" stop-color="${opts.from}"/><stop offset="1" stop-color="${opts.to}"/>
  </linearGradient></defs>
  <rect width="1200" height="630" fill="url(#g)"/>
  <rect x="60" y="150" width="${20 + opts.tag.length * 13}" height="40" rx="20" fill="${ink}"/>
  <text x="${80}" y="177" font-family="Arial, sans-serif" font-size="20" font-weight="700" fill="#ffffff">${escapeXml(opts.tag)}</text>
  ${titleSvg}
  <text x="60" y="560" font-family="Arial, sans-serif" font-size="26" font-weight="800" fill="${ink}">MyCamDesk</text>
  <circle cx="1080" cy="520" r="70" fill="${ink}" opacity="0.08"/>
  <circle cx="1130" cy="430" r="40" fill="${ink}" opacity="0.06"/>
</svg>`;
  return `data:image/svg+xml;base64,${Buffer.from(svg).toString("base64")}`;
}

function escapeXml(s: string) {
  return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

