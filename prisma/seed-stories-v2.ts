async function main() {
  let created = 0;
  for (const s of stories) {
    const existing = await prisma.article.findUnique({ where: { slug: s.slug } });
    const body = `${s.body}\n\nЧитайте также подробный гайд по теме: ${s.related}`;
    if (existing) {
      await prisma.article.update({
        where: { slug: s.slug },
        data: { title: s.title, summary: s.summary, body, coverImage: s.cover, topic: s.topic, status: "PUBLISHED" },
      });
    } else {
      await prisma.article.create({
        data: {
          title: s.title,
          slug: s.slug,
          kind: "BLOG",
          topic: s.topic,
          summary: s.summary,
          body,
          coverImage: s.cover,
          status: "PUBLISHED",
          isEditorPick: true,
          createdById: ADMIN_ID,
          publishedAt: new Date(),
        },
      });
    }
    created++;
  }
  console.log(`Upserted ${created} stories`);
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
